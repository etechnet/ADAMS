    public void setValueCell(String[] str,String nameIcon)
    {        
        remove_and_Add(jP_value,jP_value_list); 
        
        if(nameIcon == null)
            nameIcon = "";            
        
        if(jListIconValue == null)
        {
            jListIconValue = new JListIcon(ip);
            jListIconValue.set_Font(staticLib.fontA9,staticLib.fontA9);
            jListIconValue.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
            jListIconValue.setSelectionColor(java.awt.Color.white);
            jScroll_List.setViewportView(jListIconValue);
        }
        
        jListIconValue.removeAll();
        int countValori_Rows = 0;
        int Valori_Rows  = 4;
        String valori ="";
        for(int i=0;i<str.length;i++) //Aggiungo le centrali
        {
            countValori_Rows++;
            String blank = "-";
            if( (i == (str.length-1)) || (countValori_Rows == Valori_Rows) )
                blank = "";

            valori = valori + str[i] +blank;

            if( (countValori_Rows == Valori_Rows) || (i == (str.length-1)) )
            {
                jListIconValue.addElement(nameIcon,nameIcon,valori,java.awt.Color.black);
                countValori_Rows = 0;
                valori ="";
            }
        }
        jListIconValue.setSelectedIndex(0);      
    }
    
    private void remove_and_Add(java.awt.Component compRemove ,java.awt.Component compAdd)
    {
        java.awt.Component[] comps = this.getComponents();
        
        for(int i=0; i<comps.length; i++)
        {
            if( comps[i].equals(compRemove) )
            {
                remove(compRemove);
                add(compAdd,java.awt.BorderLayout.CENTER);
                break;
            }
        }
    }
    
